/*
	CREATE DATABASE A5;
	USE A5;
*/

CREATE TABLE Pol(
	polID int not null,
	pol varchar(20)
);

CREATE TABLE Svojstvo_Roditelja(
	svojstvoID int not null,
	svojstvo text
);

CREATE TABLE Roditelj(
	roditeljID int not null,
	svojstvoID int,
	ime varchar(30),
	prezime varchar(50),
	adresa varchar(50),
	fiksni_telefon varchar(15),
	mobilni_telefon varchar(20)
);

CREATE TABLE Dete(
	deteID int not null,
	polID int,
	ime varchar(30),
	prezime varchar(50),
	datum_rodjenja date,
	datum_prijema date,
	beleske text,
	roditeljID int
);

CREATE TABLE Registar_Aktivnosti(
	aktivnostID int not null,
	deteID int not null,
	datum date not null,
	prisustvo varchar(5),
	beleska text
);

CREATE TABLE Aktivnosti(
	aktivnostID int not null,
	naziv_aktivnosti varchar(50),
	dan varchar(15),
	pocetak time,
	zavrsetak time
);

/*Primary keys*/
ALTER TABLE Pol ADD CONSTRAINT PK_Pol PRIMARY KEY (polID);
ALTER TABLE Svojstvo_Roditelja ADD CONSTRAINT PK_Svojstvo_Roditelja PRIMARY KEY (svojstvoID);
ALTER TABLE Roditelj ADD CONSTRAINT PK_Roditelj PRIMARY KEY (roditeljID);
ALTER TABLE Dete ADD CONSTRAINT PK_Dete PRIMARY KEY (deteID);
ALTER TABLE Registar_Aktivnosti ADD CONSTRAINT PK_Registar_Aktivnosti PRIMARY KEY (aktivnostID,deteID,datum);
ALTER TABLE Aktivnosti ADD CONSTRAINT PK_Aktivnosti PRIMARY KEY (aktivnostID);

/*Constraints*/
ALTER TABLE Registar_Aktivnosti ADD CONSTRAINT prisustvo CHECK (prisustvo IN ('TRUE','FALSE'));

