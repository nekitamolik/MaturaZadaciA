CREATE TABLE Grad(
	gradID int IDENTITY(1,1),
	grad varchar(100),
	pozivni_broj varchar(20),
	postanski_broj varchar(20),
	broj_stanovnika int
);

CREATE TABLE Igrac(
	igracID int IDENTITY(1,1),
	ime varchar(50),
	prezime varchar(70),
	adresa varchar(100),
	gradID int,
	email varchar(50),
	telfon varchar(30)
);

CREATE TABLE Teren(
	terenID int IDENTITY(1,1),
	teren varchar(70),
	adresa varchar(100),
	gradID int,
	kontakt_telfon varchar(30)
);

CREATE TABLE Partija(
	partijaID int IDENTITY(1,1),
	terenID int,
	datum date,
	vreme_pocetka time,
	vreme_zavrsetka time,
	prvi_igracID int,
	drugi_igracID int
);

/*Primary keys*/

ALTER TABLE Grad ADD CONSTRAINT PK_Grad PRIMARY KEY (gradID);
