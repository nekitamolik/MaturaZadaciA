/*
	CREATE DATABASE A20;
	USE A20;
*/

CREATE TABLE Klijent(
	klijentID int IDENTITY(1,1),
	ime varchar(50),
	prezime varchar(70),
	adresa varchar(100),
	grad varchar(100),
	broj_telefona varchar(30)
);

CREATE TABLE Hotel(
	hotelID int IDENTITY(1,1),
	naziv varchar(70),
	adresa varchar(100),
	telefon varchar(30),
	grad varchar(100),
	drzava varchar(70),
	kategorija varchar(30)
);

CREATE TABLE Uplata(
	ugovorID int not null,
	rata int not null,
	iznos float,
	datum_uplate date
);

CREATE TABLE Turisticka_Destinacija(
	destinacijaID int IDENTITY(1,1),
	naziv_mesta varchar(70),
	drzava varchar(70),
	cena_vize float
);

CREATE TABLE Dodatni_Obilazak(
	obilazakID int IDENTITY(1,1),
	naziv varchar(70),
	cena float
);

CREATE TABLE Turisticki_Aranzman(
	ugovorID int IDENTITY(1,1),
	klijentID int,
	hotelID int,
	destinacijaID int,
	datum_polaska date,
	datum_povratka date,
	ukupna_cena_aranzmana float,
	tip_putovanja varchar(50),
	obilazakID int
);

/*Primary keys*/

ALTER TABLE Klijent ADD CONSTRAINT PK_Klijent PRIMARY KEY (klijentID);
ALTER TABLE Hotel ADD CONSTRAINT PK_Hotel PRIMARY KEY (hotelID);
ALTER TABLE Uplata ADD CONSTRAINT PK_Uplata PRIMARY KEY (ugovorID,rata);
ALTER TABLE Turisticka_Destinacija ADD CONSTRAINT PK_Turisticka_Destinacija PRIMARY KEY (destinacijaID);
ALTER TABLE Dodatni_Obilazak ADD CONSTRAINT PK_Dodatni_Obilazak PRIMARY KEY (obilazakID);
