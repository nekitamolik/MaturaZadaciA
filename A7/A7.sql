CREATE TABLE Tip(
	tipID int not null,
	naziv varchar(50),
	opis text
);

CREATE TABLE Profesor(
	profesorID int not null,
	ime varchar(40),
	prezime varchar(60),
	datum_rodjenja date,
	telefon varchar(15),
	email varchar(40)
);

CREATE TABLE Predmet(
	predmetID int not null,
	sifra_predmeta varchar(20),
	predmet varchar(50),
	semestar int,
	opis text
);

CREATE TABLE Izabrani_Predmet(
	studentID int not null,
	predmetID int not null,
	godina_slusanja int not null,
	odslusao varchar(5)
);

CREATE TABLE Student(
	studentID int not null,
	ime varchar(40),
	prezime varchar(60),
	broj_indexa varchar(15),
	datum_rodjenja date,
	adresa varchar(70),
	email varchar(40),
	telefon varchar(15)
);

CREATE TABLE Kontakt_Informacije(
	kontaktID int not null,
	studentID int,
	profesorID int,
	datum_kontakta date,
	tipID int,
	detalji text
);

