/*
	CREATE DATABASE A24;
	USE A24;
*/

CREATE TABLE Grad(
	gradID int IDENTITY(1,1),
	grad varchar(100),
	pozivni_broj varchar(30),
	postanski_broj varchar(30),
	broj_stanovnika int
);

CREATE TABLE Pacijent(
	pacijentID int IDENTITY(1,1),
	ime varchar(50),
	prezime varchar(70),
	adresa varchar(100),
	gradID int,
	broj_telefona varchar(30)
);

CREATE TABLE Poliklinika(
	poliklinikaID int IDENTITY(1,1),
	naziv varchar(100),
	adresa varchar(100),
	telefon varchar(30),
	sajt varchar(50),
	email varchar(30),
	gradID int
);

CREATE TABLE Tip_Lekara(
	tip_lekaraID int IDENTITY(1,1),
	tip varchar(50)
);

CREATE TABLE Lekar(
	lekarID int IDENTITY(1,1),
	ime varchar(50),
	prezime varchar(70),
	telefon varchar(30),
	tip_lekaraID int
);

CREATE TABLE Poliklinika_Specijalista(
	poliklinikaID int not null,
	lekarID int not null,
	aktivan varchar(5) CHECK(aktivan IN ('TRUE','FALSE'))
);

CREATE TABLE Zakazivanje(
	pacijentID int not null,
	poliklinikaID int not null,
	datum_zakazivanja date not null,
	lekarID int not null,
	komentar text
);

/*Primary keys*/

ALTER TABLE Grad ADD CONSTRAINT PK_Grad PRIMARY KEY (gradID);
ALTER TABLE Pacijent ADD CONSTRAINT PK_Pacijent PRIMARY KEY (pacijentID);
ALTER TABLE Poliklinika ADD CONSTRAINT PK_Poliklinika PRIMARY KEY (poliklinikaID);
ALTER TABLE Tip_Lekara ADD CONSTRAINT PK_Tip_Lekara PRIMARY KEY (tip_lekaraID);
ALTER TABLE Lekar ADD CONSTRAINT PK_Lekar PRIMARY KEY (lekarID);
ALTER TABLE Poliklinika_Specijalista ADD CONSTRAINT PK_Poliklinika_Specijalista PRIMARY KEY (poliklinikaID,lekarID);
ALTER TABLE Zakazivanje ADD CONSTRAINT PK_Zakazivanje PRIMARY KEY (pacijentID,poliklinikaID,datum_zakazivanja,lekarID);

