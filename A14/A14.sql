/*
	CREATE DATABASE A14;
	USE A14;
*/

CREATE TABLE Proizvodjac(
	proizvodjacID int IDENTITY(1,1),
	naziv varchar(100)
);

CREATE TABLE Pakovanje(
	pakovanjeID int IDENTITY(1,1),
	sastav text,
	kolicina int,
	naziv varchar(100),
	oblik_pakovanja varchar(100)
);

CREATE TABLE Grupa_Leka(
	grupa_lekaID int IDENTITY(1,1),
	naziv_grupe varchar(100)
);

CREATE TABLE Lek(
	lekID int IDENTITY(1,1),
	proizvodjacID int,
	naziv_leka varchar(100),
	nezasticeno_ime varchar(200),
	nacin_koriscenja text,
	grupa_lekaID int 	
);

CREATE TABLE Lek_Pakovanje(
	pakovanjeID int not null,
	lekID int not null,
	komentar text
);

CREATE TABLE Bolest(
	bolestID int IDENTITY(1,1),
	naziv varchar(100)
);

CREATE TABLE Lek_Bolest(
	lekID int not null,
	bolestID int not null,
	napomena text
);

CREATE TABLE Kontradikcija(
	kontradikcijaID int IDENTITY(1,1),
	opis text
);

CREATE TABLE Lek_Kontradikcija(
	lekID int not null,
	kontradikcijaID int not null
);

CREATE TABLE Nezeljeni_Efekat(
	nezeljeni_efekatID int IDENTITY(1,1),
	opis text
);

CREATE TABLE Lek_Nezeljeni_Efekat(
	lekID int not null,
	nezeljeni_efekatID int not null
);

