ALTER TABLE Bolest ADD CONSTRAINT PK_Bolest PRIMARY KEY (bolestID);
ALTER TABLE Lek_Bolest ADD CONSTRAINT PK_Lek_Bolest PRIMARY KEY (lekID,bolestID);
ALTER TABLE Kontradikcija ADD CONSTRAINT PK_Kontradicija PRIMARY KEY (kontradikcijaID);
ALTER TABLE Lek_Kontradikcija ADD CONSTRAINT PK_Lek_Kontradikcija PRIMARY KEY (lekID,kontradikcijaID);
