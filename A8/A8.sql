/*
	CREATE DATABASE A8;
	USE A8;
*/

CREATE TABLE Drzava (
	drzavaID int IDENTITY(1,1) not null,
	drzava varchar(70),
	pozivni_broj varchar(20),
	broj_stanovnika int
);

CREATE TABLE Grad(
	gradID int IDENTITY(1,1) not null,
	grad varchar(70),
	pozivni_broj varchar(20),
	postanski_broj varchar(20),
	broj_stanovnika int,
	drzavaID int
);

CREATE TABLE Period (
	periodID int IDENTITY(1,1) not null,
	period varchar(30),
	opis text
);

CREATE TABLE Tip_Antikviteta(
	tip_antikvitetaID  int IDENTITY(1,1) not null,
	tip varchar(70),
	slika varchar(200)
);

CREATE TABLE Arheolog(
	arheologID int IDENTITY(1,1) not null,
	ime varchar(50),
	prezime varchar(70),
	datum_rodjenja date,
	mail varchar(100),
	zivi_u text
);

CREATE TABLE Lokalitet(
	lokalitetID int IDENTITY(1,1) not null,
	koordinate_duzina float,
	koordinate_sirina float,
	opis text,
	naziv varchar(100),
	gradID int
);

CREATE TABLE Antikvitet(
	antikvitetID int IDENTITY(1,1) not null,
	periodID int,
	tip_antikvitetaID int,
	lokalitetID int,
	arheologID int,
	datum_pronalaska date,
	vreme_pronalaska time
);




